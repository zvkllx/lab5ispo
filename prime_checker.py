def is_prime(n):
    """
    Проверяет, является ли число простым.

    Параметры:
        n (int): целое число для проверки

    Возвращает:
        bool: True, если число простое, иначе False

    Примечания:
        - Числа <= 1 не являются простыми
        - Отрицательные числа не являются простыми
        - 2 и 3 - простые числа
    """
    if not isinstance(n, int):
        raise TypeError("Входное значение должно быть целым числом")
    
    # Числа меньше или равные 1 не являются простыми
    if n <= 1:
        return False
    
    # 2 и 3 - простые числа
    if n <= 3:
        return True
    
    # Чётные числа и числа, кратные 3, не являются простыми
    if n % 2 == 0 or n % 3 == 0:
        return False
    
    # Проверка делителей вида 6k ± 1
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6
    
    return True